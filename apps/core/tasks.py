import os
import shutil
from celery import shared_task
from django.utils import timezone
from datetime import timedelta

from Backend import settings
from .models import Assignment, Submission
from .utils.grading_pipeline import GradingPipeline
from django.core.mail import send_mail


@shared_task
def run_grading_task(submission_id, temp_workspace, entry_point=None):
    """
    Celery asynchronous scoring task.

    Responsibilities:
    1. Task scheduling: instantiate GradingPipeline and trigger the full grading process.
    2. Resource isolation: Runs in a separate Celery Worker process without blocking Web services.
    3. Environment cleanup: Whether scoring succeeds or fails, forcibly delete the temporary workspace generated by unpacking the source code to prevent disk overflow.

    Args:
    submission_id (int): The ID of the commit record to be corrected.
    temp_workspace (str): Path to a temporary directory to store the student source code.
    entry_point (str, optional): Analytically determined relative path to the project's main entry file.
    """
    print(f"--- Celery receives the assignment: Submission {submission_id} ---")

    try:
        pipeline = GradingPipeline(submission_id)


        if entry_point:
            pipeline.run_full_pipeline(entry_point=entry_point, work_dir=temp_workspace)
        else:
            pipeline.run_full_pipeline()

    except Exception as e:
        print(f"🚨 Celery Task Error: {str(e)}")
    finally:
        if temp_workspace and os.path.exists(temp_workspace):
            shutil.rmtree(temp_workspace, ignore_errors=True)
            print(f" Temporary directory cleared: {temp_workspace}")

# Sprint 2
@shared_task
def check_deadlines_and_send_reports():
    """
    Check the deadline and send a grade report to the teacher
    """
    now = timezone.now()
    one_hour_ago = now - timezone.timedelta(hours=1)

    pending_assignments = Assignment.objects.filter(
        deadline__lte=now,
        deadline__gte=one_hour_ago
    )

    for assignment in pending_assignments:
        teacher = assignment.teacher
        if not teacher.email:
            continue

        students = assignment.course.students.all()
        report_lines = [f"作业：{assignment.title}", f"截止时间：{assignment.deadline}", "---成绩列表---"]

        for student in students:
            submission = Submission.objects.filter(
                student=student,
                assignment=assignment,
                status='completed'
            ).order_by('-final_score').first()

            score = submission.final_score if submission else "未提交"
            report_lines.append(f"学生：{student.username} (学号：{student.student_id_num}) - 成绩：{score}")

        subject = f"【自动报告】作业《{assignment.title}》成绩统计"
        message = "\n".join(report_lines)

        send_mail(
            subject,
            message,
            settings.DEFAULT_FROM_EMAIL,
            [teacher.email],
            fail_silently=False,
        )
        print(f"已向 {teacher.username} 发送作业 {assignment.title} 的成绩报告")
